#include "nrf_gpio.h"
#include "hal_board.h"
#include "hal_led.h"



/************************************************************************************************************************************************
* define
************************************************************************************************************************************************/



/************************************************************************************************************************************************
* 函数名称：void HAL_LED_Init(hal_led_t hal_led)
* 函数说明：LED管脚初始化
* 输入参数：无
* 输出参数：无
* 返回数值：无
************************************************************************************************************************************************/
void HAL_LED_Init(hal_led_t hal_led)
{
    nrf_gpio_cfg_output(hal_led);
}

/************************************************************************************************************************************************
* 函数名称：void HAL_LED_TurnOn(hal_led_t hal_led)
* 函数说明：LED开
* 输入参数：无
* 输出参数：无
* 返回数值：无
************************************************************************************************************************************************/
void HAL_LED_TurnOn(hal_led_t hal_led)
{
    nrf_gpio_pin_clear(hal_led);
}

/************************************************************************************************************************************************
* 函数名称：void HAL_LED_TurnOff(hal_led_t hal_led)
* 函数说明：LED关
* 输入参数：无
* 输出参数：无
* 返回数值：无
************************************************************************************************************************************************/
void HAL_LED_TurnOff(hal_led_t hal_led)
{
    nrf_gpio_pin_set(hal_led);
}

/************************************************************************************************************************************************
* 函数名称：void HAL_LED_Toggle(hal_led_t hal_led)
* 函数说明：LED翻转
* 输入参数：无
* 输出参数：无
* 返回数值：无
************************************************************************************************************************************************/
void HAL_LED_Toggle(hal_led_t hal_led)
{
    nrf_gpio_pin_toggle(hal_led);
}



