/************************************************************************************************************************************************
* 函数名称：void HAL_LED_TurnOff(hal_led_t hal_led)
* 函数说明：LED关
* 输入参数：无
* 输出参数：无
* 返回数值：无
************************************************************************************************************************************************/
void HAL_LED_TurnOff(hal_led_t hal_led);

/************************************************************************************************************************************************
* 函数名称：void HAL_LED_Toggle(hal_led_t hal_led)
* 函数说明：LED翻转
* 输入参数：无
* 输出参数：无
* 返回数值：无
************************************************************************************************************************************************/
void HAL_LED_Toggle(hal_led_t hal_led);



